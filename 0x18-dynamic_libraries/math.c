#include <stdio.h>

/**
 * add - add two integers
 * @a: fisrt int
 * @b: second int
 * Return: a+b
 */

int add(int a, int b)
{
	return (a + b);
}


/**
 * sub - subtracts two integers
 * @a: fisrt int
 * @b: second int
 * Return: a-b
 */

int sub(int a, int b)
{
	return (a - b);
}

/**
 * mod - find modulus of two integers
 * @a: fisrt int
 * @b: second int
 * Return: a%b
 */

int mod(int a, int b)
{
	return (a % b);
}

/**
 * mul - multiply two integers
 * @a: fisrt int
 * @b: second int
 * Return: a*b
 */

int mul(int a, int b)
{
	return (a * b);
}

/**
 * div - divides two integers
 * @a: fisrt int
 * @b: second int
 * Return: a/b
 */

int div(int a, int b)
{
	return (a / b);
}
